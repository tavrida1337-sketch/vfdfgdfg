question() { echo -e "${COLOR_GREEN}[?]${COLOR_RESET} ${COLOR_YELLOW}$*${COLOR_RESET}"; }
reading() { read -rp " $(question "$1")" "$2"; }
error() { echo -e "${COLOR_RED}$*${COLOR_RESET}"; exit 1; }

