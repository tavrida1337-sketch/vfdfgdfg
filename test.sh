# --- ОСНОВНОЙ ЦИКЛ МЕНЮ ---

main_menu() {
    clear
    check_update_status
    show_menu

    reading "${LANG[PROMPT_ACTION]}" OPTION

    case $OPTION in
        1) manage_install ;;
        2) choose_reinstall_type ;;
        3) show_panel_node_menu ;;
        4)
            if [ ! -d "/opt/remnawave" ]; then
                echo -e "${COLOR_YELLOW}${LANG[NO_PANEL_NODE_INSTALLED]}${COLOR_RESET}"
                sleep 2
            else
                show_template_source_options
                reading "${LANG[CHOOSE_TEMPLATE_OPTION]}" TEMPLATE_OPTION
                case $TEMPLATE_OPTION in
                    1) randomhtml "simple" ;;
                    2) randomhtml "sni" ;;
                    3) randomhtml "nothing" ;;
                    0) ;;
                    *) echo -e "${COLOR_YELLOW}${LANG[INVALID_TEMPLATE_CHOICE]}${COLOR_RESET}"; sleep 1 ;;
                esac
            fi
            ;;
        5) manage_custom_legiz ;;
        6) manage_extensions ;;
        7) manage_ipv6 ;;
        8) manage_certificates ;;
        9) update_remnawave_reverse ;;
        10) remove_script ;;
        0) echo -e "${COLOR_YELLOW}${LANG[EXIT]}${COLOR_RESET}"; exit 0 ;;
        *) echo -e "${COLOR_YELLOW}${LANG[INVALID_CHOICE]}${COLOR_RESET}"; sleep 1 ;;
    esac

    # После выполнения любой функции возвращаемся в меню (рекурсия или цикл)
    main_menu
}

